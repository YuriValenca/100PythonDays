#Exercício: printar a quantidade de caracteres contidos no input do nome

print(len(input("What's your name?")))

#A função len() permite contar a quantidade de caracteres do que está dentro dela, que no caso é o input. Como os dados do usuário voltam pro input, eles são, então, repassados para o len, que tem um print aninhado a ele, logo, é printado o que está presente no len, que é a quantidade de caracteres do input! :)